    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        this.loadRol();
        User user = this.controller.getUser(id_user_update);
        
        this.txtUsername.setText(user.getUsername());
        this.txtPassword.setText(user.getPassword());
        this.cbxRol.setSelectedItem(user.getRol().getNameRol());
    }//GEN-LAST:event_formWindowOpened

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String username = this.txtUsername.getText();
        String password = new String(this.txtPassword.getPassword());
        String rol = this.cbxRol.getSelectedItem().toString();        
        
        if(username.length() == 0 || password.length() == 0) {
            showMessage("El nombre de usuario o la contrase침a no pueden quedar vacios.", "Error", "Error en la modificaci칩n del Usuario");
            return;
        }
        
        try {            
            this.controller.updateUser(id_user_update, username, password, rol);        
            showMessage("Usuario modificado correctamente!", "Information", "Modificaci칩n de nuevo Usuario correctamente");
            this.dispose();
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnSaveActionPerformed


