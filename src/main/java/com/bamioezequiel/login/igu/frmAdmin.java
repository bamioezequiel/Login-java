    private void loadData() {
        DefaultTableModel tableModel = new DefaultTableModel() {
            
            @Override
            public boolean isCellEditable (int row, int column) {
                return false;
            }
        };
        
        String titles[] = {"ID", "Usuario", "Rol"};
        tableModel.setColumnIdentifiers(titles);
        
        List<User> users = this.controller.getUsers();
        
        if(users != null) {
            for(User user : users) {
                Object[] obj = {user.getId(), user.getUsername(), user.getRol().getNameRol()};
                tableModel.addRow(obj);
            }
        }
        
        this.tDataUsers.setModel(tableModel);
    }
    
    public void showMessage(String message, String type, String title) {
        JOptionPane optionPane = new JOptionPane(message);
        if(type.equals("Information")) {
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        } else if (type.equals("Error")) {
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        JDialog dialog = optionPane.createDialog(title);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
    }
