    private void btnDeleteUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteUserActionPerformed
         if(tDataUsers.getRowCount() < 1) {
            showMessage("No hay nada para eliminar en la tabla.", "Error", "Error al eliminar en la tabla de Usuario");
            return;
        }
        if(tDataUsers.getSelectedRow() == -1) { 
            showMessage("No seleccionÃ³ ningun Usuario", "Error", "Error al eliminar en la tabla de Usuario");
            return;
        }
        
        try {
            int id_user = Integer.parseInt(String.valueOf(tDataUsers.getValueAt(tDataUsers.getSelectedRow(), 0)));
            controller.deleteUser(id_user);
            showMessage("Usuario eliminado correctamente", "Information", "Eliminar Usuario");
            loadData();    
        } catch (Exception e) {
            showMessage(e.getMessage(), "Error", "Error al eliminar en la tabla de Usuario");
        }
          

    }//GEN-LAST:event_btnDeleteUserActionPerformed

